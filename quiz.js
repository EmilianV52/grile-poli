// ─── State ───────────────────────────────────────────────────────────────────
const SET_SIZE = 10;
let activeQuestions = [];   // subset used for current quiz
let current = 0;
let answered = false;
let score = 0;
let currentCorrectIdx = 0; // position of correct answer after shuffling

// ─── Helpers ──────────────────────────────────────────────────────────────────
const app = () => document.getElementById("app");

function totalSets() {
  return Math.ceil(questions.length / SET_SIZE);
}

function getSet(idx) {
  // Returns a fixed slice of the questions array (never shuffled between sessions)
  return questions.slice(idx * SET_SIZE, idx * SET_SIZE + SET_SIZE);
}

// ─── Screen 1: Main Menu ─────────────────────────────────────────────────────
function showMenu() {
  app().innerHTML = `
    <div class="menu-screen">
      <div class="logo">🧬</div>
      <h1>Quiz Biofizică</h1>
      <p class="subtitle">${questions.length} grile · ${totalSets()} seturi · alege modul de exersare</p>

      <div class="menu-cards">
        <button class="menu-card" onclick="showSetPicker()">
          <div class="card-icon">📦</div>
          <div class="card-title">Seturi de ${SET_SIZE}</div>
          <div class="card-desc">Exersează un set fix de ${SET_SIZE} grile. Același set, mereu aceleași întrebări.</div>
        </button>

        <button class="menu-card" onclick="startRandomMode()">
          <div class="card-icon">🎯</div>
          <div class="card-title">Test sesiune</div>
          <div class="card-desc">45 de grile aleatoare — simulează condițiile unui examen real.</div>
        </button>

        <button class="menu-card" onclick="startAllMode()">
          <div class="card-icon">🔀</div>
          <div class="card-title">Toate grilele</div>
          <div class="card-desc">Parcurge toate cele ${questions.length} grile într-o singură sesiune (amestecate).</div>
        </button>
      </div>
    </div>
  `;
}

// ─── Screen 2: Set Picker ────────────────────────────────────────────────────
function showSetPicker() {
  const n = totalSets();
  let btns = "";
  for (let i = 0; i < n; i++) {
    const start = i * SET_SIZE + 1;
    const end = Math.min((i + 1) * SET_SIZE, questions.length);
    btns += `
      <button class="set-btn" onclick="startSetMode(${i})">
        <span class="set-num">${i + 1}</span>
        <span>Set ${i + 1}</span>
        <span class="set-range">Grile ${start}–${end}</span>
      </button>
    `;
  }

  app().innerHTML = `
    <div class="setpicker-screen">
      <h2>Alege un set</h2>
      <p class="sp-sub">Fiecare set conține mereu aceleași ${SET_SIZE} grile, în aceeași ordine.</p>
      <div class="set-grid">${btns}</div>
      <button class="back-btn" onclick="showMenu()">← Înapoi</button>
    </div>
  `;
}

// ─── Start quiz in SET mode (fixed questions) ────────────────────────────────
function startSetMode(setIdx) {
  activeQuestions = getSet(setIdx);
  startQuiz(`Set ${setIdx + 1} — Grile ${setIdx * SET_SIZE + 1}–${Math.min((setIdx+1)*SET_SIZE, questions.length)}`);
}

// ─── Start quiz in RANDOM 45 mode ───────────────────────────────────────────
function startRandomMode() {
  const pool = [...questions];
  shuffle(pool);
  activeQuestions = pool.slice(0, 45);
  startQuiz("Test sesiune — 45 grile");
}

// ─── Start quiz in ALL mode (all shuffled) ───────────────────────────────────
function startAllMode() {
  activeQuestions = [...questions];
  shuffle(activeQuestions);
  startQuiz("Toate grilele — " + questions.length + " grile");
}

// ─── Common quiz init ────────────────────────────────────────────────────────
function startQuiz(label) {
  current = 0;
  score = 0;
  answered = false;

  app().innerHTML = `
    <div id="progress-bar-wrap"><div id="progress-bar" style="width:0%"></div></div>
    <div style="display:flex;align-items:center;justify-content:space-between;margin-bottom:18px;">
      <span id="score-badge">Scor: 0 / 0</span>
      <span style="font-size:12px;color:#64748b;font-weight:600;">${label}</span>
    </div>
    <h2 id="nr"></h2>
    <h3 id="question"></h3>
    <div id="answers"></div>
    <div id="feedback"></div>
    <button id="nextBtn" onclick="nextQuestion()">Următoarea întrebare →</button>
  `;

  loadQuestion();
}

// ─── Quiz Logic ───────────────────────────────────────────────────────────────
function updateProgress() {
  const pct = (current / activeQuestions.length) * 100;
  document.getElementById("progress-bar").style.width = pct + "%";
  document.getElementById("score-badge").textContent =
    "Scor: " + score + " / " + current;
}

function loadQuestion() {
  answered = false;
  document.getElementById("feedback").innerHTML = "";
  document.getElementById("nextBtn").style.display = "none";

  document.getElementById("nr").innerHTML =
    "Întrebarea " + (current + 1) + " din " + activeQuestions.length;
  document.getElementById("question").innerHTML =
    activeQuestions[current].question;

  // Build a shuffled index array for answers
  const q = activeQuestions[current];
  let indices = q.answers.map((_, i) => i);
  shuffle(indices);

  // Track where the correct answer ended up after shuffle
  currentCorrectIdx = indices.indexOf(q.correct);

  let html = "";
  for (let i = 0; i < indices.length; i++) {
    const origIdx = indices[i];
    html += `
      <button class="option" id="opt-${i}" onclick="checkAnswer(${i})">
        <span class="option-dot"></span>
        <span class="option-letter">${String.fromCharCode(65 + i)}</span>
        <span class="option-text">${q.answers[origIdx]}</span>
      </button>
    `;
  }
  document.getElementById("answers").innerHTML = html;
  updateProgress();
}

function disableAllOptions() {
  document.querySelectorAll(".option").forEach(btn => {
    btn.disabled = true;
    btn.classList.add("disabled");
  });
}

function checkAnswer(choice) {
  if (answered) return;
  answered = true;

  const correct = currentCorrectIdx;  // shuffled position of correct answer
  disableAllOptions();

  document.getElementById("opt-" + correct).classList.add("correct");

  if (choice === correct) {
    score++;
    document.getElementById("feedback").innerHTML = "✅ Felicitări! Ai răspuns corect!";
    setTimeout(nextQuestion, 1200);
  } else {
    document.getElementById("opt-" + choice).classList.add("wrong");
    document.getElementById("feedback").innerHTML =
      "❌ Greșit! Răspunsul corect este <strong>" +
      String.fromCharCode(65 + correct) + "</strong>.";

    const btn = document.getElementById("nextBtn");
    btn.style.display = "inline-block";

    // Scroll the button into view so the user can always tap it
    setTimeout(() => {
      btn.scrollIntoView({ behavior: "smooth", block: "nearest" });
    }, 80);
  }
}

function nextQuestion() {
  current++;
  if (current >= activeQuestions.length) {
    const total = activeQuestions.length;
    // For sets of 10: thresholds 1-6 red, 7-8 yellow, 9-10 green
    // For other sizes: scale thresholds proportionally
    let colorClass, label, emoji;
    if (total <= 10) {
      // Absolute thresholds for sets of 10
      if (score <= 6) { colorClass = 'score-red';    label = 'Necesită mai multă exersare'; emoji = '📚'; }
      else if (score <= 8) { colorClass = 'score-yellow'; label = 'Bine, dar mai poți!';           emoji = '👏'; }
      else                  { colorClass = 'score-green';  label = 'Excelent!';                      emoji = '🏆'; }
    } else {
      // Proportional thresholds for larger sets (45 / 100 etc.)
      const pct = score / total;
      if (pct < 0.70) { colorClass = 'score-red';    label = 'Necesită mai multă exersare'; emoji = '📚'; }
      else if (pct < 0.90) { colorClass = 'score-yellow'; label = 'Bine, dar mai poți!';    emoji = '👏'; }
      else                  { colorClass = 'score-green';  label = 'Excelent!';               emoji = '🏆'; }
    }
    const barPct = Math.round((score / total) * 100);

    app().innerHTML = `
      <div class="final-screen">
        <h1>${emoji}</h1>
        <h2>Test terminat!</h2>

        <div class="final-score ${colorClass}">${score}<span> / ${total}</span></div>

        <div class="score-bar-wrap">
          <div class="score-bar ${colorClass}" style="width:0%" id="sbar"></div>
        </div>
        <div class="score-label ${colorClass}">${label} &nbsp;·&nbsp; ${barPct}% corect</div>

        <p>Ai răspuns corect la <strong>${score}</strong> din <strong>${total}</strong> întrebări.</p>

        <div style="display:flex;gap:12px;justify-content:center;flex-wrap:wrap;">
          <button class="restart-btn" onclick="location.reload()">🔄 Reîncepe</button>
          <button class="restart-btn" onclick="showMenu()">⬅ Meniu</button>
        </div>
      </div>
    `;
    // Animate the progress bar after render
    requestAnimationFrame(() => {
      requestAnimationFrame(() => {
        const bar = document.getElementById('sbar');
        if (bar) bar.style.width = barPct + '%';
      });
    });
    return;
  }
  loadQuestion();
}

function shuffle(array) {
  for (let i = array.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
}

// ─── Temă (dark / light) ─────────────────────────────────────────────────────
function initTheme() {
  const saved       = localStorage.getItem('quiz-theme');
  const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
  const theme       = saved || (prefersDark ? 'dark' : 'light');
  document.documentElement.setAttribute('data-theme', theme);
  renderToggleBtn(theme);
}

function renderToggleBtn(theme) {
  const old = document.getElementById('theme-toggle');
  if (old) old.remove();
  const btn    = document.createElement('button');
  btn.id       = 'theme-toggle';
  const isDark = theme === 'dark';
  btn.innerHTML = `<span class="tt-icon">${isDark ? '☀️' : '🌙'}</span><span>${isDark ? 'Light' : 'Dark'}</span>`;
  btn.title     = isDark ? 'Comută la modul luminos' : 'Comută la modul întunecat';
  btn.onclick   = toggleTheme;
  document.body.appendChild(btn);
}

function toggleTheme() {
  const current = document.documentElement.getAttribute('data-theme') || 'dark';
  const next    = current === 'dark' ? 'light' : 'dark';
  document.documentElement.setAttribute('data-theme', next);
  localStorage.setItem('quiz-theme', next);
  renderToggleBtn(next);
}

